// 함수 선언문
function func(num) {
  if (10 > num) {
    return "Hello";
  }
}

// 함수 호출
console.log(func(5)); // Hello

// 함수 선언문2
function sum(num1, num2) {
  return num1 + num2;
}

// 함수 호출
console.log(sum(1, 2)); // 3

